    private void btn_XacNhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_XacNhanActionPerformed
        if (cbo_BanTrong.isEnabled()) {
            // Lấy bàn trống đã chọn từ combo box
            Ban ban = (Ban) cbo_BanTrong.getSelectedItem();
            int n = JOptionPane.showConfirmDialog(this, "Chuyển dữ liệu từ bàn " + this.ban.getSoBan() + " sang bàn " + ban.getSoBan() + "?", "Xác nhận chuyển bàn", JOptionPane.YES_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (n == JOptionPane.YES_OPTION) {
                // Chuyển bàn

                // Cập nhật số bàn cho hóa đơn
                hoaDon = HoaDon_DAO.layHoaDonCuaBanDangSuDung(this.ban.getSoBan());
                hoaDon.setBan(ban.getSoBan());
                HoaDon_DAO.sua(hoaDon);                

                // Thay đổi trạng thái của 2 bàn
                Ban_DAO.thayDoiTrangThai(ban);
                Ban_DAO.thayDoiTrangThai(this.ban);
                JOptionPane.showMessageDialog(null, "Chuyển bàn thành công!");
                Fr_BanHang.load_panelBan(Fr_BanHang.trangThaiBan);
            } else {
                return;
            }
        }
        dispose();

    }//GEN-LAST:event_btn_XacNhanActionPerformed

