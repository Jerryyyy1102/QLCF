    private void load_cboBanTrong() {
        Vector<Ban> comboData = new Vector<>();
        for (Ban ban : Ban_DAO.layDanhSach(0)) {
            comboData.add(ban);
        }
        if (comboData.isEmpty()) {
            cbo_BanTrong.setEnabled(false);
            lb_HetBan.setVisible(true);
        } else {
            cbo_BanTrong.setModel(new DefaultComboBoxModel<>(comboData));
            cbo_BanTrong.setSelectedIndex(0);
        }
    }

