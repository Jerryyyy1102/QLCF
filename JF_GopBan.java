    private void load_cboBanDangSuDung() {
        Vector<Ban> comboData = new Vector<>();
        for (Ban ban : Ban_DAO.layDanhSach(1)) {
            if (ban.getSoBan() == this.ban.getSoBan()) {
                continue;
            }
            comboData.add(ban);
        }
        if (comboData.isEmpty()) {
            cbo_BanDangSuDung.setEnabled(false);
            lb_ThongBao.setVisible(true);
        } else {
            cbo_BanDangSuDung.setModel(new DefaultComboBoxModel<>(comboData));
            cbo_BanDangSuDung.setSelectedIndex(0);
            btn_Ban2.setText(((Ban) cbo_BanDangSuDung.getSelectedItem()).getSoBan() + "");
        }
    }

