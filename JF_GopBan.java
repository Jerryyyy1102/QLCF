    private void gopBan(int soBan) {
        if (cbo_BanDangSuDung.isEnabled()) {
            // Lấy bàn đã chọn từ combo box
            Ban ban = (Ban) cbo_BanDangSuDung.getSelectedItem();
            int n = JOptionPane.showConfirmDialog(this,
                    "Gộp thông tin của bàn " + this.ban.getSoBan() + " và bàn " + ban.getSoBan() + "?",
                    "Xác nhận gộp bàn", JOptionPane.YES_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (n == JOptionPane.YES_OPTION) {
                // Gộp bàn
                // Lấy hóa đơn của bàn 2
                HoaDon hoaDon2 = HoaDon_DAO.layHoaDonCuaBanDangSuDung(ban.getSoBan());
                // Tìm hóa đơn có trước và sau
                HoaDon hoaDonTruoc = new HoaDon(hoaDon2);
                HoaDon hoaDonSau = new HoaDon(hoaDon);
                if (hoaDon2.getMaHoaDon() > hoaDon.getMaHoaDon()) {
                    hoaDonTruoc = new HoaDon(hoaDon);
                    hoaDonSau = new HoaDon(hoaDon2);
                }
                // Cập nhật chi tiết hóa đơn của hóa đơn sau vào hóa đơn trước
                ArrayList<ChiTietHoaDon> list = new ArrayList<>();
                ArrayList<ChiTietHoaDon> list2 = ChiTietHoaDon_DAO.layDanhSach(hoaDonTruoc.getMaHoaDon());

                for (ChiTietHoaDon cthd : ChiTietHoaDon_DAO.layDanhSach(hoaDonSau.getMaHoaDon())) {
                    cthd.setMaHoaDon(hoaDonTruoc.getMaHoaDon());
                    for (ChiTietHoaDon e : list2) {
                        if (e.getMaSanPham() == cthd.getMaSanPham()) {
                            cthd.setSoLuong(cthd.getSoLuong() + e.getSoLuong());
                            list2.remove(e);
                            break;
                        }
                    }
                    list.add(cthd);
                }
                list2.forEach(e -> {
                    list.add(e);
                });
                // Xoá cthd cũ trong db
                ChiTietHoaDon_DAO.xoa(hoaDonTruoc.getMaHoaDon());

                // Tạo cthd mới
                for (ChiTietHoaDon cthd : list) {
                    try {
                        cthd.setTongTien(cthd.getGiaSanPham()*cthd.getSoLuong());
                        ChiTietHoaDon_DAO.them(cthd);
                    } catch (SQLException ex) {
                        Logger.getLogger(JF_GopBan.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }

                // Xoá hóa đơn sau
                ChiTietHoaDon_DAO.xoaTheoHoaDon(hoaDonSau.getMaHoaDon());
                HoaDon_DAO.xoa(hoaDonSau.getMaHoaDon());

                // Cập nhật số bàn cho hóa đơn
                hoaDonTruoc.setBan(soBan);
                HoaDon_DAO.sua(hoaDonTruoc);

                // Bỏ trạng thái đang sử dụng của bàn còn lại
                Ban_DAO.thayDoiTrangThai(soBan != ban.getSoBan() ? ban : this.ban);

                JOptionPane.showMessageDialog(null, "Gộp bàn thành công!");

                Fr_BanHang.load_panelBan(Fr_BanHang.trangThaiBan);
                dispose();
            }
        }
    }


